package kostana.maksic;

public class S4 {

	public static void main(String[] args) {
		char  ch;
		String s1 = "Zika";
		// Metoda charAt vraca karakter tipa char sa zadate pozicije u stringu. Indeksiranje ide od 0.
		ch = s1.charAt(0);
		System.out.println("Karakter na poziciji 0: " + ch);
		ch = "Java".charAt(3);
		System.out.println("Karakter na poziciji 3: " + ch);
	}

}
